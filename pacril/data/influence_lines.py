def get_standard_influence_line(influence_line_type, length, fx=10.):
    """Get standard influence line with specified length and sampling rate.

    There are currently 10 standard influence line types  available:
        1 : Total load effect
        2 : Bending moment at midspan of simply supported beam
        3 : Shear at midspand of simply supported beam
        4 : Bending moment at midspan of fixed-fixed beam
        5 : Bending moment at left support, fixed-fixed beam
        6 : Bending moment at mid of first span, two-span simply sup. beam.
        7 : Bending moment at midsupport of two-span simply sup. beam.
        8 : Bending moment at mid of second span, three-span simply sup. beam.
        9 : Bending moment at second support, three-span simply sup. beam.
       10 : Bending moment at mid of first span, three-span simply sup. beam.

    Arguments
    ---------
    influence_line_type : int
        The influence line, see table above.
    length : float
        Length of the influence line.
    fx : float
        Spatial sampling rate of influence line.
    """
    fp = INFLUENCE_LINES[influence_line_type]
    xp = np.linspace(0., 1., 101)
    N = np.round(length * fx).astype(int) + 1
    x = np.linspace(0., 1., N)
    return np.interp(x, xp, fp)




